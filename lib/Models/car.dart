class Car {
  final String id;
  final String brand;
  final String model;
  final int year;
  final int batteryRange;
  final double acceleration;
  final String imageUrl;

  Car({
    required this.id,
    required this.brand,
    required this.model,
    required this.year,
    required this.batteryRange,
    required this.acceleration,
    required this.imageUrl,
  });
}
